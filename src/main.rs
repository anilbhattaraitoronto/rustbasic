
fn main() {
    let first_num = 8;
    println!("The first number is {}", first_num);
    let mut second_num = 9;
    println!("The second number is {}", second_num);

    second_num = 12;
    println!("The reassigned second number is {}", second_num);
    const AGE: u32 = 51;
    println!("My age is {}", AGE);
}
